class Dog():
    """This is an class called Dog. It has a method called bark.
    """
    def bark(self):
        """This is a function that prints WOOF! to the console.
        """
        print("WOOF!")

mydog = Dog()  # Here we are creating an instance of the Dog class and assigning it to the variable mydog. Which simply means to assign the class to the variable.
mydog.bark()  # Here we are calling the bark method of the mydog instance of the Dog class. Which simply means to call the method of the class.
